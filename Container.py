class Container:
    feet = 0
    verticalSpeed = 0
    status = ""

    def getStatus(self):
        return self.status
    
    def setStatus(self, status):
        self.status = status

    def getVerticalSpeed(self):
        return self.verticalSpeed
    
    def setVerticalSpeed(self, verticalSpeed):
        self.verticalSpeed = verticalSpeed

    def getFeet(self):
        return self.feet
    
    def setFeet(self, feet):
        self.feet = feet
